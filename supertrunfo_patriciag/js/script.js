// funçao exibir os detalhes da carta na interface
function exibirCarta(carta){
    var cartaJogadorElement= document.getElementById('carta-jogador');
    cartaJogadorElement.querySelector('img').src = carta.imagem;
    cartaJogadorElement.querySelector('h3').textContent = carta.nome;
    // obter os atributos da carta
    var atributos = carta.atributos;
    // Criar uma string HTML com os atributos corretos
    var atributosHTML = `<p>velocidade: ${atributos.velocidade}</p> 
    <p>finalização:${atributos.finalização}</p> <p>passe:${atributos.passe}</p>`;
    // Atualiza o conteudo da DIv .carta-atributos
    cartaJogadorElement.querySelector('.carta-atributos').innerHTML = atributosHTML;
}











